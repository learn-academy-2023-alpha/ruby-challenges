class Animal 
    attr_accessor :name, :alive, :age
    def initialize (age)
        @name = name
        @alive = true
        @age = age
    end
    def age_up 
        @age =+ 1
    end
end

class Fish < Animal
    def initialize
        super(name_parameter)
        @cold_blood = true
    end
end
class Salmon < Fish
    def initialize (species, name_parameter)
        super(@cold_blood, name_parameter)
        @species = species
    end
    def get_fish_data
        if @cold_blood
        "This #{@species} of salmon is cold-blooded."
        else
        "This #{@species} of salmon is not cold-blooded."
        end
    end
end
